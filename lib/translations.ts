export function t(key: string, lang: Language): string {
  const text = translations[lang]?.[key as keyof typeof translations['en']] || translations['en'][key as keyof typeof translations['en']];
  if (!text) {
    console.warn(`Missing translation key: ${key} for language: ${lang}`);
    return key;
  }
  return text;
}

export function getLanguageFromCountry(country: string | undefined): Language {
  if (!country) return 'en';
  const c = country.toLowerCase();
  
  // German for Germany, Austria, Switzerland
  if (c.includes('germany') || c.includes('austria') || c.includes('switzerland')) return 'de';
  
  // French for France
  if (c.includes('france')) return 'fr';
  
  // Italian for Italy
  if (c.includes('italy')) return 'it';
  
  // Spanish for Spain
  if (c.includes('spain')) return 'es';
  
  // Default to English for all other countries
  return 'en';
}
